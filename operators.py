# 左右移运算符，即二进制数中位置的移动
a = 2 << 2
print(a)

# 乘方**
b = 3 ** 3
print(b)

# 或、与、异或、取反
c = 5 | 3
d = 5 % 3
e = 5 ^ 3
f = ~ 5
print(c,d,e,f)

# 小于、大于、等于、大于等于、小于等于、不等于
# 布尔“非”，布尔“与”，布尔“或” （逻辑电路）
g = 3 < 5
h = 4 == 6
i = 6 != 7
print(g,h,i,not i,g and h,g or h)

# 变量 = 变量 运算 表达式 -变成- 变量 运算 = 表达式
# 计算3的立方
j = 3
j = j * 3
j *= 3
print(j)

